/**
 * Fetch incoming Gamma Spec order messages for the admin (merchant).
 * Decrypts kind 16 messages addressed to the admin pubkey.
 */
export function useGammaOrderMessages() {
  const { nostr } = useNostr();
  const { user } = useCurrentUser();
  const adminPubkey = getAdminPubkeyHex();

  return useQuery({
    queryKey: ['gamma-orders', adminPubkey],
    queryFn: async (c) => {
      if (!user || user.pubkey !== adminPubkey) return [];

      const signal = AbortSignal.any([c.signal, AbortSignal.timeout(10000)]);
      const events = await nostr.query([
        { kinds: [16], '#p': [adminPubkey], limit: 200 },
      ], { signal });

      const decrypted: Array<{
        eventId: string;
        buyerPubkey: string;
        type: number;
        orderId?: string;
        status?: string;
        items?: GammaOrderItem[];
        raw: object;
        createdAt: number;
      }> = [];

      for (const event of events) {
        try {
          let plaintext: string | undefined;
          if (user.signer.nip44) {
            plaintext = await user.signer.nip44.decrypt(event.pubkey, event.content);
          } else if (user.signer.nip04) {
            plaintext = await user.signer.nip04.decrypt(event.pubkey, event.content);
          }
          if (!plaintext) continue;

          const msg = JSON.parse(plaintext) as Record<string, unknown>;
          const msgType = Number(msg.type);
          if (!msgType) continue;

          const orderId = event.tags.find(([t]) => t === 'order')?.[1];
          const status = event.tags.find(([t]) => t === 'status')?.[1];
          const itemTags = event.tags.filter(([t]) => t === 'item');
          const items: GammaOrderItem[] = itemTags.map(([, ref, qty]) => ({
            productRef: ref || '',
            quantity: Number(qty) || 1,
          }));

          decrypted.push({
            eventId: event.id,
            buyerPubkey: event.pubkey,
            type: msgType,
            orderId,
            status,
            items: items.length > 0 ? items : undefined,
            raw: msg,
            createdAt: event.created_at,
          });
        } catch {
          // Couldn't decrypt — skip
        }
      }

      return decrypted.sort((a, b) => b.createdAt - a.createdAt);
    },
    enabled: !!user && user.pubkey === adminPubkey,
    staleTime: 30_000,
    refetchInterval: 60_000,
  });
}

// ─── Helpers ───────────────────────────────────────────────────────────────────

/** Generate a unique order ID */
export function generateOrderId(): string {
  return `${Date.now().toString(36)}-${Math.random().toString(36).slice(2, 8)}`;
}

/** Convert a product event address to GammaOrderItem */
export function productToOrderItem(
  productId: string,
  pubkey: string,
  quantity: number,
  productName?: string
): GammaOrderItem {
  return {
    productRef: `30402:${pubkey}:${productId}`,
    quantity,
    productName,
  };
}
