/**
 * useGammaOrders — Gamma Spec order communication via NIP-17 encrypted DMs
 *
 * Order flow uses encrypted kind 16 messages with typed sub-protocols:
 *
 *   type "1" — Order Creation (buyer → merchant)
 *   type "2" — Payment Request (merchant → buyer)
 *   type "3" — Order Status Update (merchant → buyer, or buyer cancel)
 *   type "4" — Shipping Update (merchant → buyer)
 *   kind 17   — Payment Receipt (buyer → merchant)
 *   kind 14   — General Communication
 *
 * See: https://github.com/GammaMarkets/market-spec/blob/main/spec.md#4-order-communication-flow-and-payment-processing
 *
 * NIP-17 uses kind 1059 (gift wrap) for the outer envelope, but for
 * simplicity this implementation uses kind 4 (legacy NIP-04) as the
 * encryption layer since most signers support it, with a migration path
 * to full NIP-17 gift wrap when signers support it.
 */

